import { readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

function normalizeBase64(value) {
  return String(value ?? '').replace(/\s+/g, '')
}

export function normalizeTauriUpdaterPublicKey(value) {
  const normalized = normalizeBase64(value)
  if (!/^[A-Za-z0-9+/]+={0,2}$/.test(normalized) || normalized.length % 4 !== 0) {
    throw new Error('DATAPADPLUSPLUS_UPDATER_PUBKEY must be base64 text generated by Tauri.')
  }

  const decoded = Buffer.from(normalized, 'base64').toString('utf8')
  if (!decoded.startsWith('untrusted comment: minisign public key')) {
    throw new Error('DATAPADPLUSPLUS_UPDATER_PUBKEY must be the .key.pub content generated by `tauri signer generate`.')
  }

  return normalized
}

export function injectTauriUpdaterPublicKey(configPath, publicKey) {
  const absolutePath = resolve(configPath)
  const config = JSON.parse(readFileSync(absolutePath, 'utf8'))

  config.plugins = config.plugins ?? {}
  config.plugins.updater = config.plugins.updater ?? {}
  config.plugins.updater.pubkey = normalizeTauriUpdaterPublicKey(publicKey)
  config.plugins.updater.endpoints = Array.isArray(config.plugins.updater.endpoints)
    ? config.plugins.updater.endpoints
    : []

  writeFileSync(absolutePath, `${JSON.stringify(config, null, 2)}\n`)
  return config
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const [, , configPath = 'apps/desktop/src-tauri/tauri.conf.json'] = process.argv

  try {
    injectTauriUpdaterPublicKey(configPath, process.env.DATAPADPLUSPLUS_UPDATER_PUBKEY)
    console.log(`Injected updater public key into ${configPath}`)
  } catch (error) {
    console.error(error instanceof Error ? error.message : error)
    process.exit(1)
  }
}
