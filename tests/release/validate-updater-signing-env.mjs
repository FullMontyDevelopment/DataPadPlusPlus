import { fileURLToPath } from 'node:url'

const REQUIRED_ENV_VARS = [
  'DATAPADPLUSPLUS_UPDATER_PUBKEY',
  'TAURI_SIGNING_PRIVATE_KEY',
  'TAURI_SIGNING_PRIVATE_KEY_PASSWORD'
]

function normalizeBase64(value) {
  return String(value ?? '').replace(/\s+/g, '')
}

function decodeStrictBase64(name, value, errors) {
  const normalized = normalizeBase64(value)
  if (!/^[A-Za-z0-9+/]+={0,2}$/.test(normalized) || normalized.length % 4 !== 0) {
    errors.push(`${name} must be the base64 text generated by Tauri, without quotes or JSON escaping.`)
    return undefined
  }

  try {
    return Buffer.from(normalized, 'base64').toString('utf8')
  } catch {
    errors.push(`${name} is not valid base64 text.`)
    return undefined
  }
}

export function validateUpdaterSigningEnv(env = process.env) {
  const errors = []

  for (const name of REQUIRED_ENV_VARS) {
    if (String(env[name] ?? '').trim() === '') {
      errors.push(`${name} is required.`)
    }
  }

  const publicKey = decodeStrictBase64(
    'DATAPADPLUSPLUS_UPDATER_PUBKEY',
    env.DATAPADPLUSPLUS_UPDATER_PUBKEY,
    errors
  )
  if (publicKey && !publicKey.startsWith('untrusted comment: minisign public key')) {
    errors.push('DATAPADPLUSPLUS_UPDATER_PUBKEY must be the public key printed by `tauri signer generate`.')
  }

  const privateKey = decodeStrictBase64(
    'TAURI_SIGNING_PRIVATE_KEY',
    env.TAURI_SIGNING_PRIVATE_KEY,
    errors
  )
  if (privateKey && !/^untrusted comment: .*secret key/m.test(privateKey)) {
    errors.push('TAURI_SIGNING_PRIVATE_KEY must be the private key file content generated by `tauri signer generate`.')
  }

  return {
    valid: errors.length === 0,
    errors
  }
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const result = validateUpdaterSigningEnv(process.env)
  if (!result.valid) {
    for (const error of result.errors) {
      console.error(error)
    }
    process.exit(1)
  }

  console.log('Updater signing environment values have the expected Tauri key format.')
}
